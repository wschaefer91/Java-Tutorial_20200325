class WrapperDemo
{
  public static void main (String[] args)
  {
    Integer wert = new Integer(103);	// halte den Wert 103 in einem Integer-Objekt
    Double dwert = new Double(-32.78);  // halte einen double-Wert in einem Double-Objekt

    System.out.println("Integer-Objekt enthaelt: " + wert);
    System.out.println("Double-Objekt enthaelt: "  + dwert);
  }
}